# Question: What do the following expressions evaluate to?

# Solutions 

x = 2 # => 2
# Variable assignment

puts x = 2 # output 2, => nil
# puts always returns nil.

p name = "Joe" # "Joe", => "Joe"
# Variable assignment 
# Method p will output its argument and then return its argument.

four = "four" # => "four"
# Variable assignment 

print something = "nothing" # nothing=> nil
# Print will output the return value of this assignment and then return nil. Print does not append a new line onto its output. 